export const getContract = (signer) => {
  return new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);
};

export const loadCandidates = async (contract) => {
  const count = await contract.candidateCount();
  const candidateList = [];
  for (let i = 0; i < count; i++) {
    const [name, info, voteCount] = await contract.getCandidate(i);
    candidateList.push({ id: i, name, info, voteCount: Number(voteCount) || 0 });
  }
  return candidateList;
};

export const checkVoterStatus = async (contract, address) => {
  const [voted, candidateId] = await contract.getVoterVote(address);
  const registered = await contract.voters(address).then(v => v.registered);
  return { registered, voted };
};

